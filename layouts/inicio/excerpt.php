    <h2 class="excerpt_secondary_title">
        Acerca de Code Record.
    </h2>
    <p class="excerpt_paragraph">
        La clase invertida actualmente está siendo aplicada en diversas instituciones educativas a nivel mundial, siendo Platzi, EDteam y Udemy unas de las plataformas más conocidas que cubren a nivel de América Latina la educación tecnológica.
    </p>

    <p class="excerpt_paragraph">
        Este dato da cabida a la pregunta sobre ¿Cuáles son las estrategias que utilizan estas plataformas educativas para hacer tan efectiva la enseñanza para sus alumnos? Y ¿Por qué cada institución educativa debería tener una plataforma de cursos que motiven a sus estudiantes a aprender de manera autónoma?
    </p>
    
    <p class="excerpt_paragraph">
        Algunos de los cursos de este tipo de plataformas son pagadas, pero no hay que pensar que por el simple hecho de que se vaya a pagar un curso el conocimiento vendrá automáticamente a nosotros.
    </p>
    
    <p class="excerpt_paragraph">
        Si se quiere lograr un aprendizaje sistemático tenemos que tener la madurez suficiente para establecernos un tiempo, dedicado 100% a aprender con estas plataformas que vendrían a ser como un guía para el aprendizaje autónomo, a través de videos, estudiándolos fuera del horario de clases y ofreciendo una retroalimentación mediante la resolucion de problemas.
    </p>
    
    <h2 class="excerpt_secondary_title">
        Objetivo.
    </h2>

    <p class="excerpt_paragraph">
        En lugar de solo utilizar métodos tradicionales de instrucción directa, Code Record tiene la meta de aumentar el conocimiento que puede llegar a recibir el estudiante de la carrera de Pedagogía de las Ciencias Experimentales-Informática de la Facultad de Filosofía, Letras y Ciencias de la Educación de la Universidad de Guayaquil, La cual servirá de apoyo didáctico para poder sobrellevar las clases en el área de programación, exigiendo como unico requisito para los estudiantes una constancia practica para que la conceptualización y la aplicación de los contenidos sean útiles en su vida profesional.
    </p>
    
